void ddaline(float x1,float y1,float x2,float y2)
{

	float m,mh,xcur,ycur;
	if(x1<0 || x2<0 || y1<0 || y2<0 || x1>getmaxx() || x2>getmaxx() || y1>getmaxy() || y2>getmaxy())
	{
		printf("\nCoordinate value(s) out of bound");
		getch();
		exit(0);
	}
	xcur=x1;
	ycur=y1;
	if(x1==x2)
	{
		if(y1<=y2)
		{
			while(ycur<=y2)
			{
				putpixel(xcur,ycur,WHITE);
				ycur++;
			}
		}
		else
		{
			while(ycur>=y2)
			{
				putpixel(xcur,ycur,WHITE);
				ycur--;
			}
		}
	}else if(y1==y2)
	{
		if(x1<=x2)
		{
			while(xcur<=x2)
			{
				putpixel(xcur,ycur,WHITE);
				xcur++;
			}
		}
		else
		{
			while(xcur>=x2)
			{
				putpixel(xcur,ycur,WHITE);
				xcur--;
			}
		}

	}
	else
	{
		m=(y2-y1)/(x2-x1);
		//printf("\n\n%f",m);
		if(m<=1)
		{
			if(x1<x2)
			{
				while(xcur<=x2)
				{
					putpixel(xcur,ycur,WHITE);
					ycur=ycur+m;
					xcur++;
				}
			}
			else
			{
				while(xcur>=x2)
				{
					putpixel(xcur,ycur,WHITE);
					ycur=ycur-m;
					xcur--;
				}
			}
		}else{
			mh=1/m;
			if(y1<y2)
			{
				while(ycur<=y2)
				{
					putpixel(xcur,ycur,WHITE);
					xcur=xcur+mh;
					ycur++;
				}
			}
			else
			{
				while(ycur>=y2)
				{
					putpixel(xcur,ycur,WHITE);
					xcur=xcur-mh;
					ycur--;
				}
			}
		}
	}
}